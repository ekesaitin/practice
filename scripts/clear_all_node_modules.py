from os import *
from os.path import *
import shutil


def remove_node(dir):
    if not isdir(dir):
        return

    name = split(dir)[1]

    if name == 'node_modules':
        shutil.rmtree(dir)
        return

    for d in listdir(dir):
        remove_node(join(dir, d))


remove_node(getcwd())
